def main():
    p = Presentation(str(PPTX))
    applied, missed = 0, []
    for (si, shi, pi, ri), new_text in PATCHES.items():
        try:
            slide = p.slides[si - 1]
            shape = slide.shapes[shi]
            run = shape.text_frame.paragraphs[pi].runs[ri]
            old = run.text
            run.text = new_text
            applied += 1
            print(f'  ✓ s{si}.sh{shi}.p{pi}.r{ri}: |{old}| → |{new_text}|')
        except Exception as e:
            missed.append((si, shi, pi, ri, str(e)))
            print(f'  ✗ s{si}.sh{shi}.p{pi}.r{ri}: {e}')
    p.save(str(PPTX))
    print(f'\n適用: {applied}件 / 失敗: {len(missed)}件')
    print(f'保存: {PPTX}')

if __name__ == '__main__':
    main()
